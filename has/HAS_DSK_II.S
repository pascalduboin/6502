* HAS_DISK_II
* SCAN SLOTS FOR
* FOR DISK II CARDS
*
* P. DUBOIN
* 1 NOV 2024
* LISA 2.5
*#--------------->
*
;
** ZPAGE
PTR EPZ 6
MASK EPZ 8
DSK_LST EPZ 9
;
** ROM
HOME EQU $FC58
PRBYTE EQU $FDDA
;
 ORG $800
 OBJ $800
;
HAS_DSK2:
 LDA #$80
 STA MASK
 LDA #0
 STA DSK_LST
 STA PTR
 LDX #7
;
NXT_II:
 TXA
 ORA #$C0
 STA PTR+1
;
 LDY #1
 LDA (PTR),Y
 CMP #$20
 BNE NO_II
;
 LDY #3
 LDA (PTR),Y
 BNE NO_II
;
 LDY #5
 LDA (PTR),Y
 CMP #3
 BNE NO_II
;
 LDY #$FF
 LDA (PTR),Y
 BNE NO_II
 LDA DSK_LST
 ORA MASK
 STA DSK_LST
;
NO_II:
 LSR MASK
 DEX
 BNE NXT_II
;
 JSR HOME
 LDA DSK_LST
 JMP PRBYTE
;
 END
